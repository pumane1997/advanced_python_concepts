
'''
Here is a quick summary of inheritance with a minimalistic example.

Class Small:
    pass
 
Class Bigger(Small):
    pass
In the above example, class Bigger inherits from class Small. We can also say:

Bigger subclasses Small.

Bigger extends Small.

Bigger is the child. Small is the parent.

And don't forget, Bigger has access to all the methods of Small 
(if there were any methods in Small).
'''